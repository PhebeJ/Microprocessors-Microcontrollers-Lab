ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	a db 01h,02h,03h,04h,05h
DATA ENDS
CODE SEGMENT
START:
		MOV AX,DATA
		MOV DS,AX
		MOV DL,00H
		MOV DH,00H
		LEA SI,a
		MOV CL,05H
	L:	MOV AX,000H
		MOV AL,[SI]
		MOV BL,02H
		DIV BL

		CMP AH,00h
		JNZ OD
		JMP EVE
 	B: INC SI
		DEC CL
		JNZ L
		
		MOV AH,4CH
		INT 21H


OD:  INC DL
		JMP B

EVE: INC DH
		JMP B

CODE ENDS
END START
