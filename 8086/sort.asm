ASSUME CS:CODE,DS:DATA
DATA SEGMENT
	a db 02h,01h,05h,04h
DATA ENDS
CODE SEGMENT
START:	
		MOV AX,DATA
		MOV DS,AX
		MOV CL,03H
OUTT:	MOV CH,03H
		LEA SI,a
INN:	MOV AH,[SI]
		MOV AL,[SI+1]
		CMP AH,AL	
		JG SWA
	EE:	INC SI
		DEC CH
		JNZ INN
		DEC CL
		JNZ OUTT	
		
		LEA SI,a
		MOV AH,[SI]
		MOV BH,[SI+1]
		MOV CH,[SI+2]
		MOV DH,[SI+3]
		MOV AH,4CH
		INT 21H

SWA:  MOV [SI],AL
		MOV [SI+1],AH
		JMP EE

CODE ENDS
END START
